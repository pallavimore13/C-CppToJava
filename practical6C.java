class Main
{
	public static void main(String[] args)
	{
		int value = 3;
		
		switch(value)
		{
			case 1:
				System.out.println("Value is 1");
				break;
			case 2:
				System.out.println("Value is 2");
				break;
			case 3:
				System.out.println("Value is 3");
				break;
			case 4:
				System.out.println("Value is 4");
				break;
			default:
				System.out.println("Value is other than 1,2,3,4");
		}
	}
}
